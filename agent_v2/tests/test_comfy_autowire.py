def test_resolve_literal_returns_direct_literal() -> None:
    graph = load("kandinsky_t2v")
    assert resolve_literal(graph, "12:8", "seed") == ("12:8", "seed")


def test_resolve_literal_follows_primitive_int_indirection() -> None:
    graph = load("ace_step")
    # KSampler.seed is wired to PrimitiveInt "109" — writing it directly would
    # sever the wire.
    assert resolve_literal(graph, "3", "seed") == ("109", "value")


def test_resolve_literal_rejects_derived_size() -> None:
    graph = load("capybara_edit")
    with pytest.raises(_Unresolved, match="GetImageSize"):
        resolve_literal(graph, "103:78", "width")


def test_resolve_literal_rejects_generated_text() -> None:
    graph = load("longcat_redraw")
    with pytest.raises(_Unresolved, match="OllamaGenerateV2"):
        resolve_literal(graph, "13:4", "text")


def test_resolve_literal_rejects_concat_of_generated_text() -> None:
    graph = load("capybara_i2v")
    # 103:44.text ← StringConcatenate(129) ← OllamaGenerateV2(128)
    with pytest.raises(_Unresolved, match="StringConcatenate"):
        resolve_literal(graph, "103:44", "text")


def test_resolve_literal_rejects_missing_input() -> None:
    graph = load("kandinsky_t2v")
    with pytest.raises(_Unresolved, match="no input"):
        resolve_literal(graph, "12:8", "nonexistent")


# --------------------------------------------------------------------------
# wan_i2v — WanImageToVideo, two-stage KSamplerAdvanced, ReActor face swap
# --------------------------------------------------------------------------


def test_wan_i2v() -> None:
    params, notes = guess_params_ex(load("wan_i2v"), "img2video")

    # The old slot-blind trace walked into WanImageToVideo(63) and returned the
    # *positive* encoder for the negative wire ["63", 1]; negative was then dropped.
    assert params["negative"] == [["7", "text"]]

    # 62 is the scene image (→ ReActor.input_image → WanImageToVideo.start_image);
    # 126 is the ReActor face reference.
    assert params["input_image"] == [["62", "image"]]
    assert params["face_swap"] == [["126", "image"]]

    # 58 has add_noise: "disable" — its noise_seed never affects the output.
    assert params["seed"] == [["57", "noise_seed"]]

    assert params["steps"] == [["57", "steps"], ["58", "steps"]]
    assert params["cfg"] == [["57", "cfg"], ["58", "cfg"]]
    assert params["width"] == [["63", "width"]]
    assert params["height"] == [["63", "height"]]
    assert params["length"] == [["63", "length"]]
    assert params["fps"] == [["109", "fps"]]

    # The positive prompt is generated by an Ollama chain — nothing to overwrite.
    assert params["prompt"] == []
    assert "129" in notes["prompt"]


