def test_node_order_sorts_ints_numerically_then_composites() -> None:
    assert node_order(["103:17", "9", "114", "103:8", "62"]) == [
        "9",
        "62",
        "114",
        "103:17",
        "103:8",
    ]


def test_live_subgraph_excludes_preview_only_branches() -> None:
    graph = load("longcat_redraw")
    live = live_subgraph(graph)
    # 20 is PreviewAny — a dead end hanging off the Ollama node.
    assert "20" not in live
    # 13:8 (VAEDecode) feeds SaveImage; 19 (Ollama) feeds the positive encoder.
    assert {"9", "13:8", "13:7", "19"} <= live


def test_live_subgraph_falls_back_to_whole_graph_without_output_node() -> None:
    graph = {"1": {"class_type": "PreviewImage", "inputs": {}}}
    assert live_subgraph(graph) == {"1"}


def test_live_subgraph_drops_dead_preview_image_branch() -> None:
    graph = load("wan_i2v")
    live = live_subgraph(graph)
    assert "127" not in live  # PreviewImage
    assert "118" not in live  # PreviewAny


