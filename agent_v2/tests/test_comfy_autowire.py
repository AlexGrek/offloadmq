_ALL = [
    ("wan_i2v", "img2video"),
    ("capybara_i2v", "img2video"),
    ("kandinsky_t2v", "txt2video"),
    ("capybara_edit", "img2img"),
    ("longcat_redraw", "txt2img"),
    ("ace_step", "txt2music"),
]


@pytest.mark.parametrize(("fixture", "task_type"), _ALL)
def test_every_target_is_an_existing_literal_slot(fixture: str, task_type: str) -> None:
    """No target may point at a missing node, a missing input, or a wired slot.

    Writing into a wired slot is what silently corrupts the graph at execution time.
    """
    graph = load(fixture)
    params, _ = guess_params_ex(graph, task_type)
    for field, targets in params.items():
        assert targets is not None
        for node_id, input_name in targets:
            assert node_id in graph, f"{field}: node {node_id} missing"
            inputs = graph[node_id]["inputs"]
            assert input_name in inputs, f"{field}: {node_id} has no input {input_name}"
            value = inputs[input_name]
            assert not isinstance(value, list), f"{field}: {node_id}.{input_name} is a wire"


@pytest.mark.parametrize(("fixture", "task_type"), _ALL)
def test_autodetect_is_deterministic(fixture: str, task_type: str) -> None:
    graph = load(fixture)
    first = guess_params_ex(graph, task_type)
    for _ in range(5):
        assert guess_params_ex(load(fixture), task_type) == first


@pytest.mark.parametrize(("fixture", "task_type"), _ALL)
def test_unresolved_fields_are_empty_and_explained(fixture: str, task_type: str) -> None:
    params, notes = guess_params_ex(load(fixture), task_type)
    for field, targets in params.items():
        if targets == []:
            assert field in notes, f"{field} is unwired but has no note"
        assert "FIXME" not in json.dumps(targets)
