"""Autowiring against six real ComfyUI API-format graphs.

Fixtures in ``fixtures/comfy/`` are verbatim exports with long string literals
replaced by ``<text N chars>`` placeholders — only the graph topology matters here.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any, Dict

import pytest

from offloadmq_core.comfy_autowire import (
    _Unresolved,
    guess_params_ex,
    live_subgraph,
    node_order,
    resolve_literal,
)

FIXTURES = Path(__file__).parent / "fixtures" / "comfy"


def load(name: str) -> Dict[str, Any]:
    with open(FIXTURES / f"{name}.json", encoding="utf-8") as f:
        graph: Dict[str, Any] = json.load(f)
    return graph


